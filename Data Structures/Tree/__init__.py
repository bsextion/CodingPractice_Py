class LinkedList:
    def __init__(self):
        self.head = None
class Node:
    def __init__(self, data=None,next = None):
        self.data = data
        self.next = next


class Tree:
    def __init__(self):
        self.root = None
class TreeNode:
    def __init(self, data, left=None,right=None):
        self.data = data
        self.left = left
        self.right = right